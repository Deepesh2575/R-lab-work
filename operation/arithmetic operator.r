a <- 15
b <- 2
add_result <- a + b
cat("Addition: ", add_result, "\n")
sub_result <- a - b
cat("Subtraction: ", sub_result, "\n") 
mul_result <- a * b
cat("Multiplication: ", mul_result, "\n")
div_result <- a / b
cat("Division: ", div_result, "\n")
mod_result <- a %% b
cat("Modulus: ", mod_result, "\n")
exp_result <- a ^ b
cat("Exponentiation: ", exp_result, "\n")
int_div_result <- a %/% b
cat("Integer Division: ", int_div_result, "\n")