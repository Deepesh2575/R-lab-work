a <- 10
b <- 20
cat("a = ", a, "\n")
cat("b = ", b, "\n")
cat("Is a equal to b? :", (a == b), "\n")
cat("Is a not equal to b? :", (a != b), "\n")
cat("Is a greater than b? :", (a > b), "\n")
cat("Is a less than b? :", (a < b), "\n")
cat("Is a greater than or equal to b? :", (a >= b), "\n")
cat("Is a less than or equal to b? :", (a <= b), "\n")